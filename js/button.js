
document.getElementById("card-1").addEventListener('click', function(){
    alert('Board updated Successfully');
    const task = document.getElementById("task-no").innerText;
    const convertedTask = parseInt(task);

    const sum = convertedTask - 1;
    document.getElementById("task-no").innerText = sum;
    const point = document.getElementById("point").innerText;
    const convertedPoint = parseInt(point);
    const sum2 = convertedPoint + 1;
    document.getElementById("point").innerText = sum2;

    const history = document.getElementById("history");

    let date = new Date();

    const p = document.createElement("p");
    p.innerText = "You have Complete The Task Fix Mobile Button Issue at " + date
    history.appendChild(p);
    this.disabled = true;
    document.getElementById("card-1").classList.add("bg-gray-400");
})
